class Operators{
	public static void main(String[]args){
	int a = 5;
	int v1 = a++;
	int v2 = ++a;
	int v3 = a--;
	int v4 = --a;
	System.out.println("The value of v1 is : "+v1);
	System.out.println("The value of v2 is : "+v2);
	System.out.println("The value of v3 is : "+v3);
	System.out.println("The value of v4 is : "+v4);
	}
}